
package Classes;


public class Calculations {
    
  public static int addNumbers(int x,int y) {
   return x+y;      
  }
  public static int subNumbers(int x, int y) {
    return x-y;
  }
  public static int multiplyNumbers(int x, int y) {
      return x*y;
  }
  public static double divideNumbers(double x, double y) {
      return x/y;
  }
}
